const buttons = document.querySelectorAll(".btn-food");
const category = document.querySelector(".category");

category.innerHTML = Sushi;

buttons.forEach(function (btn) {
  btn.addEventListener("click", function () {
    buttons.forEach(function (b) {
      b.classList.remove("active");
    });

    btn.classList.add("active");

    if (btn.id === "Steak") category.innerHTML = Steak;
    if (btn.id === "Sushi") category.innerHTML = Sushi;
    if (btn.id === "Pasta") category.innerHTML = Pasta;
    if (btn.id === "Appetizer") category.innerHTML = Appetizer;
  });
});

//////////////////////////////////////////////////////////////////////////
// start Animation 
const slides = document.querySelectorAll("#dining-slider .slide");
let current = 0;

setInterval(() => {
  const currentSlide = slides[current];
  currentSlide.classList.remove("active");
  currentSlide.classList.add("exit-left");

  current = (current + 1) % slides.length;
  const nextSlide = slides[current];
  nextSlide.classList.add("active");

  setTimeout(() => {
    currentSlide.classList.remove("exit-left");
  }, 700);

}, 3000); 
